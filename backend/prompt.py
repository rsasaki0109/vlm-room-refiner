"""VLM 向けシステム／ユーザープロンプト。"""

ANALYZE_SYSTEM = """あなたは熟練のインテリアコーディネーターです。画像に写っている部屋の現状を観察し、\
具体的で実行可能な提案に落とし込みます。抽象的な言い回しは避け、部屋のレイアウト・配色・物の置き方に触れてください。

重要:
- 画像に写っていない情報は断定しない（必要なら「仮定：」で短く明示）
- problems で「窓がない」「日が入らない」と断定しない（画像の端に窓が写っていないだけの可能性）。外壁・開口が写っていない場合は採光課題に触れるなら「自然光が弱く見える」などに留める
- リフォーム級の提案（壁を壊す/窓や開口を増やす/配線工事/造作/床や壁の塗装・張り替え）はしない。小物・配置・照明・収納で改善する
- 賃貸想定で原状回復が難しい作業は避ける（やるなら「貼って剥がせる」「置くだけ」「突っ張り」「スタンドタイプ」等に限定）
- 読みやすい日本語で、誤字や冗長さを避ける"""

ANALYZE_USER_TEMPLATE = """この部屋の画像を分析し、与えられたJSONスキーマに厳密に従うキーのみを出力してください。

次の方針で埋めてください:
- room_type: 部屋の用途が推定できる範囲で具体的に（曖昧な「居室」は避け、分からなければ「小規模住居系」と理由を含めて短く）
- style: 画像から読み取れる要素に基づき、スタイルを名付けて推定
- problems: 視認できる問題を最大5件。各項目1文で、配線、収納、採光、配色、家具サイズ感など「写真から言えること」
- recommendations: 最大5件。各項目は「何を/どこに/どれくらい」で具体化（例: “壁沿いに配線モール”“2700Kの暖色ライト”“幅60cm・奥行30cmのラック”）
  - Before/After 形式にする場合も、工事・新規開口・壁埋め込み・ビス穴が必須の提案は書かない（例: 「窓を増やす」「壁掛けTVの固定穴」は不可）
  - 「設置」と書くときは置き型・クリップ・テープ・突っ張り・床置き等、原状回復しやすい手段に限定
  - テレビは「テレビ台」「キャスター付きスタンド」「壁面ラック（突っ張り）」など、ビス不要の選択肢を優先
  - 寸法は画像から読み取れない場合「目安：幅60cm前後」のようにレンジ表現にする（断定しない）
  - 電気工事が必要な提案（天井埋め込み・新規コンセント等）は避け、スタンドライト・クランプライト・電球交換などに寄せる
- shopping_keywords: 各項目は検索用の短いフレーズ（カテゴリ+サイズ/色/素材/特徴）。予算がある場合は価格帯のヒントも可。「窓」「リフォーム」「工事」など工事連想語は入れない

制約:
- 日本語
- 各配列は1〜5要素
- 推測に根拠が薄いことは、短く「仮定：」で明示
- 禁止: 「窓を設置」「開口を増やす」「壁を壊す」「大規模配線工事」「壁に穴を開ける」などの工事系提案
- 禁止: 「壁紙を貼る」「塗装」「床材/フローリング張り替え」「天井照明を施工」「シーリングファン工事」など原状回復が難しい提案
- 禁止: 「整理整頓して」だけの提案（必ず具体的行動に落とす）
- 禁止: 画像の外観から判断できない“素材/色/間取り”の断定（必要なら「仮定：」）
"""


def build_user_prompt(
    style_target: str | None = None,
    budget: str | None = None,
    want_before_after: bool = False,
) -> str:
    """拡張用: 目標スタイル・予算・Before/After を任意で追記。"""
    parts = [ANALYZE_USER_TEMPLATE.rstrip()]

    if style_target and style_target.strip():
        parts.append(
            f"\n【目標の雰囲気】ユーザーは「{style_target.strip()}」に近づけたい。可能な範囲で recommendations / shopping_keywords に反映。"
        )
    if budget and budget.strip():
        parts.append(
            f"\n【予算感】{budget.strip()}。無理のない価格帯を shopping_keywords / recommendations に意識。"
        )
    if want_before_after:
        parts.append(
            "\n【補足】recommendations の中に、現状（Before）と目指す完成イメージ（After）を一文ずつ入れる等、対比が分かる表現を1件以上入れてください。"
        )

    return "\n".join(parts) + "\n"
